console.log(1===1 && 2===2); // true && true = true
console.log(1===1 && 2===3); // true && false = false

console.log(1===1 || 2===2); // true || true = true
console.log(1===1 || 2===3); // true || false = true

console.log(1!=1); // false
console.log(1!=2); // true

console.log(!(1===1)); // !true = false
console.log(!(1===2)); // !false = true
